public class conditionalOPerator {

    public static void main(String[] args) {
        int temp = 21;
        if (temp > 30) {
            System.out.println("a/c on");
        } else if (temp < 22){
            System.out.println("a/c off");
        } else {
            System.out.println("a/c standby");
        }
    }
}
